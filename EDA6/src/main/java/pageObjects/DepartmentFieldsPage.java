package pageObjects;

import org.openqa.selenium.By;
import org.openqa.selenium.WebDriver;
import org.openqa.selenium.WebElement;

public class DepartmentFieldsPage {

	public WebDriver driver;
	
	public By Fields_on_table=By.xpath("//*[@id='dtBasicExampleField']/tbody/tr[2]/td[1]");
	public By Add=By.xpath("//*[@id='add_field']");
	public By View=By.xpath("//*[@id='view_field']");
	public By Edit=By.xpath("//*[@id='edit_field']");
	public By Inctive=By.xpath("//*[@id='Field_Active']");
	public By Rview=By.xpath("//*[@id='viewfield']/a");
	public By Redit=By.xpath("//*[@id='editfield']/a");
	public By Active=By.xpath("//*[@id='FieldActive']/a/span");
	
	
	
	public DepartmentFieldsPage(WebDriver driver) {
		
		this.driver=driver;
	}
	
	public WebElement getFields_on_table() {
		
		return driver.findElement(Fields_on_table);
	}
	
	public WebElement getAdd() {
		
		return driver.findElement(Add);
	}
	
	public WebElement getView() {
		
		return driver.findElement(View);
	}
	
	public WebElement getEdit() {
	
	return driver.findElement(Edit);
	}
	
	public WebElement getInctive() {
	
	return driver.findElement(Inctive);
	}
	
	public WebElement getRview() {
	
	return driver.findElement(Rview);
	}

	public WebElement getRedit() {
	
		return driver.findElement(Fields_on_table);
	}
	
	public WebElement getActive() {
		
		return driver.findElement(Active);
	}
	
}
